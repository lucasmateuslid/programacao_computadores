n1 = 100 # Variavel com valor inteiro, que inicia a contagem em 100
while n1 <= 1000: # Deterima uma repetição, que deve respeitar a condição enquanto verdade. Se for falso interrompe o fluxo.
    print(n1)
    n1+=2 # Irá adicionar 2 ao valor de n1, para que a contagem avance de 2 em 2.
# O loop irá parar quando o valor de n1 for maior que 1000. Senão continuará até chegar ao limite!